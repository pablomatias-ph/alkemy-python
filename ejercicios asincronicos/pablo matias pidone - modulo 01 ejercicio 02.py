# Crear una lista con 5 elementos 
mi_lista = ['lunes', 'martes', 'miercoles', 'jueves', 'viernes']
print (mi_lista)

# Imprimir el último elemento
print (mi_lista[4])

# Modificar el valor del tercer elemento
mi_lista[2] = 'wednesday'
print (mi_lista)

# Agregar dos elementos
mi_lista.append('sabado')
mi_lista.append('domingo')
print (mi_lista)

# Eliminar algún elemento
mi_lista.pop(2)
print (mi_lista)